package basico

fun main() {
    // operador de atribuição
    val nomeCompleto = "Gabriel Rodrigues dos Santos"

    var x = 10

    // x = x + 2
    x += 2
    println(x)

    // x = x - 2
    x -= 2
    println(x)

    // x = x * 2
    x *= 2
    println(x)

    // x = x / 2
    x /= 2
    println(x)

    // x = x + 1
    x++
    println(x)
}