package basico

fun main() {
    val primeiroNumero = 22
    val segundoNumero = 11

    // igual
    println(primeiroNumero == segundoNumero)

    // diferente de
    println(primeiroNumero != segundoNumero)

    // maior que
    println(primeiroNumero > segundoNumero)

    // menor que
    println(primeiroNumero < segundoNumero)

    // maior ou igual a
    println(primeiroNumero >= segundoNumero)

    // menor ou igual a
    println(primeiroNumero <= segundoNumero)
}