package basico

fun main() {
    val primeiroValor = 22
    val segundoValor = 10

    // soma
    println("soma: ${ primeiroValor + segundoValor }")

    // subtração
    println("subtração: ${ primeiroValor - segundoValor }")

    // multiplicação
    println("multiplicação: ${ primeiroValor * segundoValor }")

    // divisão
    println("divisão: ${ primeiroValor / segundoValor }")

    // resto da divisão
    println("resto: ${ primeiroValor % segundoValor }")
}